    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //      Valideren inputs
        $request->validate([
            'name' => 'required|min:3|max:255',
            'email' => 'required|min:3|max:255|unique:users',
            'password' => 'required|min:8|max:50|confirmed',
            'password_confirmation' => 'required|min:8|max:50'
        ]);

        //      Opslaan inputs in Usertable
        $storeUser = new User();
        $storeUser->name = $request->get('name');
        $storeUser->email = $request->get('email');
        $storeUser->password = $request->get('password');
        $storeUser->save();

        //      Return Home
        return redirect('/home');
    }

